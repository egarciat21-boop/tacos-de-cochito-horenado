    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if (tipoSeleccionado.isEmpty() || txtDescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione tipo y escriba descripci√≥n XD Porfa.");
            return;
        }
        Reclamo r = new Reclamo(tipoSeleccionado, txtDescripcion.getText());
        listaReclamos.add(r);
        guardarReclamos();
        actualizarLista();
        txtDescripcion.setText("");
        JOptionPane.showMessageDialog(this, "Reclamo agregado.");
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int index = lstReclamos.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un reclamo para editar no sea como negro.");
            return;
        }
        Reclamo r = listaReclamos.get(index);
        String nuevaDesc = JOptionPane.showInputDialog("Editar reclamo:", r.getDescripcion());
        if (nuevaDesc != null) {
            r.setDescripcion(nuevaDesc);
            guardarReclamos();
            actualizarLista();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = lstReclamos.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un reclamo para eliminar :3 .");
            return;
        }
        listaReclamos.remove(index);
        guardarReclamos();
        actualizarLista();
    }//GEN-LAST:event_btnEliminarActionPerformed

