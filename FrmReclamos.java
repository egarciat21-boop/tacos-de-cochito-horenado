    private void guardarReclamos() {
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("reclamos.dat"))) {
            oos.writeObject(listaReclamos);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
        }
    }

    private void cargarReclamos() {
        File archivo = new File("reclamos.dat");
        if (!archivo.exists()) return;

        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream(archivo))) {
            listaReclamos = (ArrayList<Reclamo>) ois.readObject();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar: " + e.getMessage());
        }
    }

    private void actualizarLista() {
        modeloLista.clear();
        for (Reclamo r : listaReclamos) {
            modeloLista.addElement(r.toString());
        }
    }
