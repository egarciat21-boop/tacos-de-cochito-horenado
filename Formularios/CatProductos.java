    private void jButton1GuardarProducActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1GuardarProducActionPerformed
        Productos pro = new Productos();
        pro.setNombre(jTextField2Nom.getText());//obtener texto ingresado
        pro.setTalla(jTextField4Talla.getText());
        try {
            double precio = Double.parseDouble(jTextField5Precio.getText().trim());
            pro.setPrecio(precio);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero valido en el campo Precio.");
            return; //si el valor no es v√°lido
        }
        try {
            int stock = Integer.parseInt(jTextField1Stock.getText().trim());
            pro.setStock(stock);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero valido en el campo Stock.");
            return;
        }
        pro.setEstado(jTextField2Estado.getText());
        pro.setCategoria(jTextField3Categ.getText());
        
        if (pro.Guardar()) //variable conexion a BD
            JOptionPane.showMessageDialog(this, "Producto Creado", "Informacion", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this, "Producto NO creado", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1GuardarProducActionPerformed

